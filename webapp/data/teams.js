// 根据小组获取球队
function getTeamsByGroup(group) {
    return Object.entries(teamsData)
        .filter(([_, team]) => team.group === group)
        .reduce((acc, [key, team]) => {
            acc[key] = team;
            return acc;
        }, {});
}

// 根据ID获取球队
function getTeamById(id) {
    return teamsData[id] || null;
}

// 搜索球队
function searchTeams(query) {
    const lowerQuery = query.toLowerCase();
    return Object.entries(teamsData)
        .filter(([_, team]) =>
            team.name.toLowerCase().includes(lowerQuery) ||
            team.flag.includes(query)
        )
        .reduce((acc, [key, team]) => {
            acc[key] = team;
            return acc;
        }, {});
}
