// 获取所有比赛
function getAllMatches() {
    return matchesData;
}

// 根据小组获取比赛
function getMatchesByGroup(group) {
    if (group === 'all') return matchesData;
    return matchesData.filter(match => match.group === group);
}

// 根据轮次获取比赛
function getMatchesByRound(round) {
    if (round === 'all') return matchesData;
    return matchesData.filter(match => match.round === parseInt(round));
}

// 根据球队获取比赛
function getMatchesByTeam(teamId) {
    return matchesData.filter(match =>
        match.homeTeam === teamId || match.awayTeam === teamId
    );
}

// 根据ID获取比赛
function getMatchById(id) {
    return matchesData.find(match => match.id === id);
}
