// 根据球队获取球员
function getPlayersByTeam(teamId) {
    return playersData.filter(player => player.team === teamId);
}

// 根据位置获取球员
function getPlayersByPosition(position) {
    if (position === 'all') return playersData;
    return playersData.filter(player => player.position === position);
}

// 搜索球员
function searchPlayers(query) {
    const lowerQuery = query.toLowerCase();
    return playersData.filter(player =>
        player.name.toLowerCase().includes(lowerQuery) ||
        player.nameEn.toLowerCase().includes(lowerQuery) ||
        player.teamName.toLowerCase().includes(lowerQuery)
    );
}

// 根据ID获取球员
function getPlayerById(id) {
    return playersData.find(player => player.id === id);
}
